export default function FAQPage() {
  const [openFaq, setOpenFaq] = useState<number | null>(null);

  // Ομαδοποίηση των FAQs ανά κατηγορία
  const groupedFaqs = faqs.reduce((acc, faq) => {
    acc[faq.category] = acc[faq.category] || [];
    acc[faq.category].push(faq);
    return acc;
  }, {} as Record<string, typeof faqs>);

  return (
    <>
      <Head>
        <title>Συχνές Ερωτήσεις – ThessVolt</title>
        <meta
          name="description"
          content="Απαντήσεις στις πιο συχνές ερωτήσεις σχετικά με τις υπηρεσίες της ThessVolt στη Θεσσαλονίκη."
        />
      </Head>

      <section className="min-h-screen bg-white text-[#033941] py-12 px-6">
        <h1 className="text-4xl font-bold text-center text-yellow-400 mb-10">Συχνές Ερωτήσεις</h1>
        <div className="max-w-4xl mx-auto space-y-10">

          {Object.entries(groupedFaqs).map(([category, faqs], groupIdx) => (
            <div key={groupIdx}>
              <h2 className="text-2xl font-semibold text-yellow-400 mb-6">{category}</h2>

              <div className="space-y-4">
                {faqs.map((faq, index) => (
                  <div key={index} className="border border-[#033941] rounded-md overflow-hidden">
                    <button
                      onClick={() => setOpenFaq(openFaq === index + groupIdx * 100 ? null : index + groupIdx * 100)}
                      className="w-full flex items-center justify-between p-4 text-lg font-medium focus:outline-none"
                    >
                      {faq.question}
                      <span className={`transform transition-transform ${openFaq === index + groupIdx * 100 ? "rotate-180" : ""}`}>
                        ▼
                      </span>
                    </button>
                    {openFaq === index + groupIdx * 100 && (
                      <div className="p-4 text-gray-700 bg-yellow-50">
                        {faq.answer}
                      </div>
                    )}
                  </div>
                ))}
              </div>
            </div>
          ))}

        </div>
      </section>
    </>
  );
}
